namespace App\Console\Commands\Politica;

use Illuminate\Console\Command;
use App\Models\Politica\Cidade;

class CorrectCoordinates extends Command
{
    protected $signature = 'politica:correct-coordinates';
    protected $description = 'Corrige as coordenadas de latitude e longitude de todas as cidades da Bahia usando o NOME da cidade.';

    public function handle()
    {
        $this->info('Iniciando a correção final das coordenadas geográficas por nome...');
        $coordinates = $this->getBahiaCoordinates();
        $progressBar = $this->output->createProgressBar(count($coordinates));
        $notFound = [];

        foreach ($coordinates as $nome_cidade => $coords) {
            $cidade = Cidade::where('nome', $nome_cidade)->first();

            if ($cidade) {
                $cidade->update([
                    'latitude' => $coords['lat'],
                    'longitude' => $coords['lon'],
                ]);
            } else {
                $notFound[] = $nome_cidade;
            }
            $progressBar->advance();
        }

        $progressBar->finish();
        $this->info("\nCorreção concluída.");

        if (!empty($notFound)) {
            $this->warn('Não foi possível encontrar as seguintes cidades pelo nome: ' . implode(', ', $notFound));
        }

        return 0;
    }

